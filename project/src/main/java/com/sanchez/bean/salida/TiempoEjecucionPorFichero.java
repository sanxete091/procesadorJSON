package com.sanchez.bean.salida;

public class TiempoEjecucionPorFichero {

	private String nombreFicheroProcesado;
	private Long tiempoEjecucion;
	public String getNombreFicheroProcesado() {
		return nombreFicheroProcesado;
	}
	public void setNombreFicheroProcesado(String nombreFicheroProcesado) {
		this.nombreFicheroProcesado = nombreFicheroProcesado;
	}
	public Long getTiempoEjecucion() {
		return tiempoEjecucion;
	}
	public void setTiempoEjecucion(Long tiempoEjecucion) {
		this.tiempoEjecucion = tiempoEjecucion;
	}
	
	
}
